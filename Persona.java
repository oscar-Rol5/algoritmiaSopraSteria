
public class Persona {

	int edad;
	String sexo;
	
	Persona(int edad, String sexo) {
		super();
		this.edad = edad;
		this.sexo = sexo;
	}

	public int getEdad() {
		return edad;
	}
	
	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	public String getSexo() {
		return sexo;
	}
	
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	
}
